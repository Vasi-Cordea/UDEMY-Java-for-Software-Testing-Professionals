package pack;

public final class LogicalOperators {

	public static void main(String[] args) {
		boolean b1 = true;
		boolean b2 = false;
		
		System.out.println(b1&&b2); // && AND operator
		System.out.println(b1||b2); // || OR operator
		System.out.println(!b1); // || ! NOT operator
		System.out.println(!b2); // || 
	}

}
